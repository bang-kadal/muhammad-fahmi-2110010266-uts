    private void buttonMasukkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMasukkanActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // Memeriksa apakah salah satu dari kolom teks yang diperlukan kosong
        if (textNama.getText().trim().equals("") || textAlamat.getText().trim().equals("") || textNohp.getText().trim().equals("") || textPekerjaan.getText().trim().equals("")){
        // Menampilkan pesan kesalahan jika salah satu kolom kosong
            JOptionPane.showMessageDialog(null, "data yang anda masukkan belum benar \nSilahkan isi dengan benar");
        }else{
           // Jika semua kolom yang diperlukan diisi, tambahkan baris baru ke model tabel
            model.addRow(new Object[]{textNama.getText(),textAlamat.getText(),textNohp.getText(),textPekerjaan.getText(),jComboBox1.getSelectedItem()});
           // Menampilkan pesan keberhasilan
            JOptionPane.showMessageDialog(null, "data berhasil dimasukkan");
        }
        // Mengosongkan kolom input setelah pemrosesan
            textNama.setText("");
            textAlamat.setText("");
            textNohp.setText("");
            textPekerjaan.setText("");
        // Mengatur kembali item yang dipilih di jComboBox1 menjadi string kosong
            jComboBox1.setSelectedItem("");
        
    }//GEN-LAST:event_buttonMasukkanActionPerformed

