    private void buttonHapusTabelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusTabelActionPerformed
       
        
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRow()==-1) {
            if (jTable1.getSelectedRowCount()==0) {
                JOptionPane.showMessageDialog(null, "pilih data yang anda inginkan");
            }else{
                JOptionPane.showConfirmDialog(null, "pilih data yang ingin anda hapus");
            }
        }else{
            model.removeRow(jTable1.getSelectedRow());
            JOptionPane.showConfirmDialog(null, "data sudah berhasil terpilih");
        }
        textNama.setText("");
        textAlamat.setText("");
        textNohp.setText("");
        textPekerjaan.setText("");
        jComboBox1.setSelectedItem("");

    }//GEN-LAST:event_buttonHapusTabelActionPerformed

    private void buttonSimpantabelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpantabelActionPerformed
       
        
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        if (jTable1.getRowCount()==-1){
        if (jTable1.getRowCount()==0){
                JOptionPane.showConfirmDialog(null, "pilih data yang ingin anda simpan");
            }else{
                JOptionPane.showMessageDialog(null, "pilih data yang ingin anda hapus");
         }
        }else{
            model.setValueAt(textNama.getText(), jTable1.getSelectedRow(), 0);
            model.setValueAt(textAlamat.getText(), jTable1.getSelectedRow(), 1);
            model.setValueAt(textNohp.getText(), jTable1.getSelectedRow(), 2);
            model.setValueAt(textPekerjaan.getText(), jTable1.getSelectedRow(), 3);
            model.setValueAt(jComboBox1.getSelectedItem(), jTable1.getSelectedRow(), 4);
            buttonMasukkan.setEnabled(true);
        JOptionPane.showMessageDialog(null, "data tabel yang anda pilih berhasil di edit");
        textNama.setText("");
        textAlamat.setText("");
        textNohp.setText("");
        textPekerjaan.setText("");
        jComboBox1.setSelectedItem("");
        }
    }//GEN-LAST:event_buttonSimpantabelActionPerformed

